# КАЛЬКУЛЯТОР
num = int(input('Введите 1 число: '))
num2 = int(input('Введите 2 число: '))
act = input('Введите знак "действия" над числами: ')

if act == '+':
    print(f'Результат сложения = {num + num2}')
elif act == '-':
    print(f'Результат вычитания = {num - num2}')
elif act == '*':
    print(f'Результат произведения = {num * num2}')
elif act == '/':
    if num2 != 0:
        print(f'Результат частного = {num / num2}')
    else:
        print('На 0 делить нельзя!')
elif act == '**':
    print(f'Результат возведения в степень = {num ** num2}')
elif act == '//':
    if num2 != 0:
        print(f'Результат деления без остатка = {num // num2}')
    else:
        print('На 0 делить нельзя!')
elif act == '%':
    if num2 != 0:
        print(f'Результат нахождения остатка от деления = {num % num2}')
    else:
        print('На 0 делить нельзя!')
else:
    print('ошибка')
